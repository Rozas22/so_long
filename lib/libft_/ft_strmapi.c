#include "libft.h"

char	*ft_strmapi(char const *s, char (*f) (unsigned int, char))
{
	unsigned int	i;
	unsigned int	len;
	char			*res;

	if (s == NULL || f == NULL)
		return (NULL);
	len = ft_strlen(s);
	res = malloc((len +1) * (sizeof(char)));
	if (!res)
		return (NULL);
	i = 0;
	while (i < len)
	{
		res[i] = (*f)(i, s[i]);
		i++;
	}
	res[i] = '\0';
	return (res);
}
